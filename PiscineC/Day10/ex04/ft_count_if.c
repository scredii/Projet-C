int		ft_count_if(char **tab, int (*f)(char*))
{
	int a;
	int b;

	a = 0;
	b = 0;
	while (tab[a] != 0)
	{
		if (f(tab[a]) == 1)
			b++;
		a++;
	}
	return (b);
}
