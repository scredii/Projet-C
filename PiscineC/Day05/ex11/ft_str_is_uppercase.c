int		ft_str_is_uppercase(char *str)
{
	int a;

	a = 0;
	while (str[a])
	{
		if ((str[a] >= 0 && str[a] <= 64) || (str[a] >= 91 && str[a] <= 122))
			return (0);
		else if (str[a] == '\0')
			return (1);
		a++;
	}
	return (1);
}
