int		ft_str_is_printable(char *str)
{
	int a;

	a = 0;
	while (str[a])
	{
		if ((str[a] >= 0 && str[a] <= 31) || str[a] == 127)
			return (0);
		else if (str[a] == '\0')
			return (1);
		a++;
	}
	return (1);
}
