int		ft_atoi(char *str)
{
	int a;
	int sign;

	sign = 0;
	a = 0;
	while (*str == ' ')
	{
		str++;
	}
	if (*str == '+')
		str++;
	else if (*str == '-')
	{
		sign = 1;
		str++;
	}
	while ((*str <= '9') && (*str >= '0'))
	{
		a = a * 10 - (*str - '0');
	}
	if (sign != 0)
		a = -a;
	return (a);
}
